#include <iostream>

using namespace std;

int main() {;

	cout << "0xB6" << endl; // a의 값 <--- 예시정답

	cout << "0x42" << endl; // k의 값

	cout << "0xB6" << endl; // t의 값

	cout << "0x1F" << endl; // c의 주소

	cout << "0x1F" << endl; // *a

	cout << "6" << endl; // **p

	cout << "0x05" << endl; // &k

	cout << "0x42" << endl; //*p

	return 0;

}